#include <stdio.h>

int main() {
    char x;
    fflush(stdin);
    fflush(stdout);
    printf("Enter a character: \n");
    fflush(stdin);
    fflush(stdout);
    scanf(" %c", &x);
    fflush(stdin);
    fflush(stdout);
    printf("You entered %c\n", x);
    return 0;
}

